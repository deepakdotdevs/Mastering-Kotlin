// Strings are immutable. Once you initialize a string, you can't
// change its value or assign a new value to it. All operations that
// transform strings return their results in a new String object, leaving the original string unchanged.

fun main() {
    val str = "Hello, Friend 123"
    println(str)
    println(str[0])
    println(str[1])
    println(str[2])
    println(str[3])
    println(str[4])
    println(str[5])
    println(str[6])
    println(str[7])
    println(str[8])

    println("Hiiii !! 951***578")
}