// Mostly Used Data Types : 

Int (Integer) -> 4 bytes -> eg; 10, 20, 30
Long (Integer)-> 8 bytes -> eg; 10000000000L
Float (Floating Point)-> 4 bytes -> eg; 10.5F, 147.85f
Double (Floating Point) -> 8 bytes -> eg; 10478542.5, 1447854267.85, 12.4, 10.1
Char (Character) -> 2 bytes -> eg; 'A', 'B', 'C'
String (Word / Sentences)-> 2 bytes per character -> eg; "Hello", "Kotlin", "Data Types", "123456789"
Boolean (True / False) -> 1 byte -> eg; true, false
